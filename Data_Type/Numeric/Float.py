"""
This code demonstrates the use of float data types in Python. 
It defines several float variables, including a price, temperature, 
and two numbers in scientific notation. 
The code then prints each variable along with its data type using the `type()` function.
"""

#Float: Numbers with a decimal point or exponential notation.

price = 23.48
temp = 36.85
scientific_1 = 1.23e4
scientific_2 = 5.67e-3

print("Price:", price, "Data Type:", type(price))
print("Temperature:", temp, "Data Type:", type(temp))
print("Scientific Notation 1:", scientific_1, "Data Type:", type(scientific_1))
print("Scientific Notation 2:", scientific_2, "Data Type:", type(scientific_2))