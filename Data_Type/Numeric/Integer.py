"""
This code demonstrates the use of integer data types in Python. 
It defines several integer variables, including positive and negative numbers, zero, 
a large population number, a binary number, and a hexadecimal number. 
The code then prints each variable along with its data type using the `type()` function.
"""
# Integer = whole numbers without a fractional part, which can be positive, negative, or zero.

age = 29
num = -72
zero = 0
population = 892148216
binary_num = 0b101010
hex_num = 0x1A


print("Age:", age, "Data Type:", type(age))
print("Negative Number:", num, "Data Type:", type(num))
print("Zero:", zero, "Data Type:", type(zero))
print("Population:", population, "Data Type:", type(population))
print("Binary Number:", binary_num, "Data Type:", type(binary_num))
print("Hexadecimal Number:", hex_num, "Data Type:", type(hex_num))