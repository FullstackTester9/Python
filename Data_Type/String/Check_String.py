"""
This is a simple Python program that demonstrates how to check for 
the presence of a phrase or character in a string.
"""


str = "I'm leaning Python."

# Check phrase or a character is present in the string.

print("Is 'n' in the string?: ", "n" in str)

print("Is 'N' in the string?: ", "N" in str) # Case sensitive.

print("Is 'Python' in the string?: ", "Python" in str)

# Check phrase or a character is not present in the string.

print("Is 'm' not in the string?: ", "m" not in str)