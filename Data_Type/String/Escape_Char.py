"""
This code demonstrates the use of escape characters in Python strings. 
Escape characters allow you to include special characters in strings 
that would otherwise be difficult or impossible to represent directly.
"""

# Printing a single quote using escape character
print("Printing a single quote: ", "\'", end='\n\n')

# Printing a double quote using escape character
print("Printing a double quote: ", "\"", end='\n\n')

# Printing a backslash using escape character
print("Printing a backslash: ", "\\", end='\n\n')

# Printing a new line using escape character
print("Printing a new line using escape character: ", "Hello\nWorld!", end='\n\n')

# Printing a tab using escape character
print("Printing a tab using escape character: ", "Hello\tWorld!", end='\n\n')

# Printing a carriage return using escape character
print("Printing a carriage return using escape character: ", "Hello\rWorld!", end='\n\n')

# Printing a form feed using escape character
print("Printing a form feed using escape character: ", "Hello\fWorld!", end='\n\n')

# Printing a backspace using escape character
print("Printing a backspace using escape character: ", "Hello\bWorld!", end='\n\n')

# Printing a vertical tab using escape character
print("Printing a vertical tab using escape character: ", "Hello\vWorld!", end='\n\n')

# Printing an octal value using escape character
print("Printing an octal value using escape character: ", "\101", end='\n\n')

# Printing a hexadecimal value using escape character
print("Printing a hexadecimal value using escape character: ", "\x48", end='\n\n')

# Printing a Unicode character using escape character
print("Printing a Unicode character using escape character: ", "\u0036", end='\n\n')